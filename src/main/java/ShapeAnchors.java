public class ShapeAnchors {
    public double entryX;
    public double entryY;
    public double exitX;
    public double exitY;

    public ShapeAnchors(double entryX, double entryY, double exitX, double exitY) {
        this.entryX = entryX;
        this.entryY = entryY;
        this.exitX = exitX;
        this.exitY = exitY;
    }
}
